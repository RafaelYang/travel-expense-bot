/**
 * 帶參數的翻譯：用 {key} 佔位
 */
export function interpolate(text: string, params?: Record<string, string>): string {
  if (!params) return text
  return Object.entries(params).reduce(
    (result, [key, value]) => result.replace(`{${key}}`, value),
    text
  )
}
